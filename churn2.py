"""#3. Classification Models (5 algorithms)"""

# Helper to train + tune + evaluate one classifier
def run_classifier(model, param_grid, name):
    print(f"\n--- Training {name} ---")
    grid = GridSearchCV(model, param_grid, cv=3, scoring='roc_auc', n_jobs=-1)
    grid.fit(X_train_final, y_train_final)
    best = grid.best_estimator_
    y_pred = best.predict(X_test)
    y_proba = best.predict_proba(X_test)[:,1]

    acc = accuracy_score(y_test, y_pred)
    prec = precision_score(y_test, y_pred)
    rec = recall_score(y_test, y_pred)
    f1 = f1_score(y_test, y_pred)
    auc = roc_auc_score(y_test, y_proba)
    train_time = grid.cv_results_['mean_fit_time'][grid.best_index_]

    print(f"{name} - AUC: {auc:.4f}, Acc: {acc:.4f}, Time: {train_time:.3f}s")
    return best, {'Model':name, 'Accuracy':acc, 'Precision':prec, 'Recall':rec, 'F1':f1, 'ROC-AUC':auc, 'Time':train_time}, y_proba

