# Helper for regression
def run_regressor(model, param_grid, name):
    print(f"\n--- Training {name} ---")
    grid = GridSearchCV(model, param_grid, cv=3, scoring='neg_mean_squared_error', n_jobs=-1)
    grid.fit(Xr_train, yr_train)
    best = grid.best_estimator_
    y_pred = best.predict(Xr_test)
    mae = mean_absolute_error(yr_test, y_pred)
    mse = mean_squared_error(yr_test, y_pred)
    rmse = np.sqrt(mse)
    r2 = r2_score(yr_test, y_pred)
    n = len(yr_test)
    p = Xr_test.shape[1]
    adj_r2 = 1 - (1-r2)*(n-1)/(n-p-1)
    train_time = grid.cv_results_['mean_fit_time'][grid.best_index_]
    print(f"{name} - RMSE: {rmse:.2f}, R²: {r2:.4f}, Time: {train_time:.3f}s")
    return best, {'Model':name, 'MAE':mae, 'MSE':mse, 'RMSE':rmse, 'R²':r2, 'Adj R²':adj_r2, 'Time':train_time}, y_pred

